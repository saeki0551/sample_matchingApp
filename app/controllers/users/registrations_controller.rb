  def create
    if params[:user].present?
      redirect_to new_user_information_path
    else
      binding.pry
      ActiveRecord::Base.transaction do
        user = User.call_user
        user.save!
        user_information.user_id = user.id
        user_information.save!
      end
      sign_in(user)
      redirect_to users_path, notice: "アカウントの作成に成功しました。"
    end
  end
    
